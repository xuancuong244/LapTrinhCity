    /**
     * Hien gio
     */
    @Override
    public void run() {
        while (true) {
            Date date = new Date();
            SimpleDateFormat sdf = new SimpleDateFormat();
            sdf.applyPattern("hh:mm:ss aa");
            String time = sdf.format(date);
            lblClock.setText(time);
            try {
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                break;
            }
        }
    }

