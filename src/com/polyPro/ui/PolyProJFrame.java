    /**
     * Mo form thong ke
     *
     * @param index la tabs thong ke muon mo
     */
    private void openThongKe(int index) {
        if(XShare.authenticated()){
            if(index == 3 && !XShare.isManager()){
                XDialog.alert(this, "You can't access");
                return;
            }
            ThongKeJFrame tkwin = new ThongKeJFrame();
            tkwin.selectTab(index);
            tkwin.setVisible(true);
        } else {
            XDialog.alert(this, "Please login!");
        }
    }

